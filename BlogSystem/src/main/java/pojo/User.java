package pojo;

public class User {
    private String uid;
    private String uname;
    private String pwd;

    public void setUid(String uid) {
        this.uid = uid;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }

    public void setPwd(String pwd) {
        this.pwd = pwd;
    }
}
